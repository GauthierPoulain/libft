int			ft_math_sqrt(int nb)
{
	int		i;

	if (nb < 0 || nb > 46340 * 46340)
		return (0);
	i = 1;
	while (i * i < nb)
		i++;
	if (i * i == nb)
		return (i);
	return (0);
}
